// 根据百分比获取强度描述
export function getIntensityLabel(percentage: number): string {
  if (percentage >= 80) return '重度'
  if (percentage >= 60) return '中度'
  return '轻度'
}

// 根据隐藏属性获取标签
export function getHiddenTags(hidden: { O: number; A: number; C: number; H: number }): string[] {
  const tags: string[] = []
  tags.push(hidden.O > hidden.A ? '纠结' : '果断')
  tags.push(hidden.C > hidden.H ? '高冷' : '温暖')
  return tags
}
